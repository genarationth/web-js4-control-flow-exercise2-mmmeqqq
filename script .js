let dayNumber = 1 ;

switch (true) {
    case dayNumber == 0 :
        console.log('Sunday');
        break;
        case dayNumber == 1 :
        console.log('Monday');
        break;
        case dayNumber == 2 :
        console.log('Tueday');
        break;
        case dayNumber == 3 :
        console.log('Wedday');
        break;
        case dayNumber == 4 :
        console.log('Thursday');
        break;
        case dayNumber == 5 :
        console.log('Friday');
        break;
        case dayNumber == 6 :
        console.log('Saturday');
        break;
}